    /**
     * Return array of states that's not exist in the database
     *
     * @return array
     */
    private function safeData()
    {
        $states = $this->phpMyAdminExport();
        $data   = [];

        foreach ($states as $state) {
            $current = model('state')->grabId($state['id']);

            if ($current->exists) {
                continue;
            }
            $data[] = [
                 'id'         => $state['id'],
                 'created_at' => $state['created_at'],
                 'updated_at' => $state['updated_at'],
                 'deleted_at' => $state['deleted_at'],
                 'title'      => $state['title'],
                 'slug'       => ($state['slug'] ?? ''),
                 'parent_id'  => $state['parent_id'],
                 'capital_id' => $state['capital_id'],
                 'meta'       => [
                      'latitude'  => $state['latitude'],
                      'longitude' => $state['longitude'],
                      'weather'   => null,
                 ],
            ];
        }
        return $data;
    }



    /**
     * Find states that's latitude is null
     */
    private function findAndUpdateStates()
    {
        if (Schema::hasColumn('states', 'meta')) {
            $states = yasna()->model('State')->get();
            foreach ($states as $state) {
                if (is_null($state->latitude) || strlen($state->latitude) <= 2) {
                    $this->updateState($state);
                }
            }
        } else {
            $this->command->error('States table does not have meta field. please migrate the yasna module.');
        }
    }



    /**
     * Update the latitude and longitude of specified state
     *
     * @param State $state
     */
    private function updateState($state)
    {
        $id           = $state->id;
        $parent_id    = $state->parent_id;
        $weather      = $state->weather;
        $local_states = $this->phpMyAdminExport();
        $local_state  = collect($local_states)->where('id', $id)->where('parent_id', $parent_id)->first();
        $state->update([
             'meta' => [
                  'latitude'  => $local_state['latitude'],
                  'longitude' => $local_state['longitude'],
                  'weather'   => $weather ?? null,
             ],
        ]);
    }



